// String methods = Allow you to manipulate and work with text (strings)

let userName = 'Kunal';

console.log(userName.charAt(0)); //Return K

console.log(userName.indexOf('u')); //Returns 1

console.log(userName.length); //Prints the length of the string

userName = userName.trim(); //trim the space from the starting and the end
console.log(userName)

userName = userName.toUpperCase(); //Change the characters to uppercase
console.log(userName);

userName = userName.repeat(2); //Repeats the string 2 times
console.log(userName);

console.log(userName.startsWith('K')); //Returns true



